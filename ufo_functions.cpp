void end_game(string answer, string codeword) {
  bool won = answer == codeword;
  if (won) {
    cout << "Hooray! You saved the person and earned the medal of honor!" << endl;
  } else {
    cout << "Oh no! The UFO just flew away with another person!" << endl;
  }
}

void display_status(string answer, vector<char> incorrect) {
    cout << "Incorrect Guesses: " << endl;
    for (int i=0; i < incorrect.size(); i++) {
      cout << incorrect[i] << " ";
    }
    cout << endl;
    cout << "Codeword: " << endl;
    for (int i=0; i < answer.size(); i++) {
      cout << answer[i];
    }
    cout << endl;
}

